-- "Разобрали" (DESIGN.md §4.1) — товар закончился, но остаётся видимым в
-- каталоге в особом состоянии (серое фото, бейдж "Разобрали", кнопка
-- "Сообщить о завозе" вместо "В корзину"), в отличие от is_active = false,
-- который убирает товар из выдачи полностью (WHERE p.is_active = true в
-- /api/catalog) — это другая, уже существующая семантика, её не трогаем.
--
-- Фронт (ProductCard.jsx) уже читает product.inStock === false и полностью
-- отрисовывает это состояние — просто никогда не получал такое значение,
-- т.к. колонки не было. DEFAULT true — у всех существующих товаров ничего
-- не меняется, пока админ явно не отметит конкретный товар как закончившийся.
--
-- Применить: node migrations/apply.js 027_product_in_stock.sql

ALTER TABLE products ADD COLUMN IF NOT EXISTS in_stock BOOLEAN NOT NULL DEFAULT true;
