-- настройка "в заказе всегда 1 товар" и тихо продолжал бы давать старый
-- завышенный расчёт. NULL — явный сигнал "не настроено", calcPricing() на
-- фронте показывает объясняющее сообщение, как и для planned_sales_monthly = 0.
--
