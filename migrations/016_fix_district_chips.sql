-- Чипы районов в шторке адреса брали данные из таблицы districts, куда
-- попали станции метро ("Новоясеневская", "Тютчевская") и улица
-- ("Генерала Тюленева") вместе с настоящими районами. Эта таблица не
-- участвует в проверке зоны доставки (это делает отдельная delivery_zones
-- с полигонами) — она только для списка чипов, так что деактивация
-- не-районов ничего не ломает.
-- Применить вручную: psql ... -f 016_fix_district_chips.sql

UPDATE districts SET is_active = false
WHERE name IN ('Новоясеневская', 'Тютчевская', 'Генерала Тюленева');
