-- Цвет бейджа товара ("Хит", "Выгодно" и т.п.) был захардкожен в коде
-- фронтенда (HitBadge.jsx / EcoBadge.jsx) и не настраивался через админку.
-- Добавляем колонку рядом с существующими badge_type/badge_label — NULL
-- означает "использовать цвет по умолчанию для этого типа" (обратная
-- совместимость: у всех текущих товаров эта колонка будет NULL, внешний
-- вид не меняется, пока админ явно не выберет цвет).
--
-- Значение — не свободный RGB, а один из 4 предустановленных акцентов
-- дизайн-системы (см. DESIGN.md §1): хранится как готовый hex, который
-- отдаётся фронтенду и применяется напрямую, без маппинга на стороне клиента.
--   #1C8F1C — зелёный   (--color-accent)
--   #D07812 — оранжевый (--color-sage)
--   #153F15 — тёмно-зелёный (--color-hero-bg)
--   #5A5550 — серый     (--color-text-soft, как у бейджа "Разобрали")
-- Ограничение choice-листом не накладываем на уровне БД (TEXT, не ENUM) —
-- ограниченный выбор обеспечивает сам <select> в админке, как и для
-- badge_type/category; так и остальные текстовые enum-подобные поля в
-- этой схеме не имеют DB-level CHECK.
--
-- Применить: node migrations/apply.js 026_product_badge_color.sql

ALTER TABLE products ADD COLUMN IF NOT EXISTS badge_color TEXT;
